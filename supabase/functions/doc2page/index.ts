serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { imageBase64, fileName } = await req.json();
    
    if (!imageBase64) {
      return new Response(
        JSON.stringify({ success: false, error: 'No image data provided' }),
        { status: 400, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    const hfToken = Deno.env.get('HUGGING_FACE_ACCESS_TOKEN');
    let result: Doc2PageResult;

    try {
      if (hfToken) {
        console.log("Calling Doc2Page HF Space API...");
        const html = await callDoc2PageAPI(imageBase64, hfToken);
        result = {
          success: true,
          html,
          pages: [html],
          usedFallback: false,
          model: 'PaddlePaddle/doc2page'
        };
      } else {
        throw new Error("No HF token configured");
      }
    } catch (apiError) {
      console.warn("Doc2Page API failed, using fallback:", apiError);
      
      // Generate mock HTML page with watermarks
      const mockHtml = generateMockHtmlPage(imageBase64);
      
      result = {
        success: true,
        html: mockHtml,
        pages: [mockHtml],
        usedFallback: true,
        model: 'mock-fallback'
      };
    }

    return new Response(
      JSON.stringify(result),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error) {
    console.error('Doc2Page error:', error);
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: error instanceof Error ? error.message : 'Unknown error' 
      }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
