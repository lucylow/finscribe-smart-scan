import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

// HF Space API endpoint for PaddlePaddle/doc2page
const DOC2PAGE_API_URL = "https://paddlepaddle-doc2page.hf.space/api/predict";

interface Doc2PageResult {
  success: boolean;
  html?: string;
  pages?: string[];
  error?: string;
  usedFallback: boolean;
  model: string;
}

async function callDoc2PageAPI(imageBase64: string, hfToken: string): Promise<string> {
  // Try calling the Gradio Space API
  const response = await fetch(DOC2PAGE_API_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${hfToken}`,
    },
    body: JSON.stringify({
      data: [`data:image/jpeg;base64,${imageBase64}`]
    }),
  });

  if (!response.ok) {
    throw new Error(`HF API returned ${response.status}: ${await response.text()}`);
  }

  const result = await response.json();
  console.log("Doc2Page raw response:", JSON.stringify(result).substring(0, 500));

  // Parse various response formats from Gradio
  if (result.data && Array.isArray(result.data) && result.data[0]) {
    return result.data[0];
  }
  if (result.html) {
    return result.html;
  }
  if (result.output) {
    return result.output;
  }
  
  throw new Error("Unexpected response format from Doc2Page");
}

